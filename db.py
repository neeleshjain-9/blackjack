def return_card(card):
    suit = card[0]
    num = card[1]
    if suit == "♠":
        return Card_art["spades"][num]
    elif suit == "♦":
        return Card_art["diamonds"][num]
    elif suit == "♥":
        return Card_art["hearts"][num]
